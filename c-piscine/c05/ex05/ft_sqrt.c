int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	unb;

	i = 0;
	unb = (unsigned int)nb;
	while (i * i <= unb)
	{
		if (i * i == unb)
			return (i);
		i++;
	}
	return (0);
}
