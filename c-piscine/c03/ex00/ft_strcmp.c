int	ft_strcmp(char *s1, char *s2)
{
	int	cnt;
	int	chk;

	cnt = 0;
	chk = 0;
	while (s1[cnt] != '\0')
	{
		if (s1[cnt] != s2[cnt])
		{
			chk = s1[cnt] - s2[cnt];
			return (chk);
		}
		cnt++;
	}
	if (s2[cnt] > '\0')
		return (-s2[cnt]);
	return (0);
}
