int	ft_atoi(char *str)
{
	int			n;
	int			i;
	long long	l;

	n = 1;
	i = 0;
	l = 0;
	while ((*str >= 9 && *str <= 13) || (*str == ' '))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			n *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		l *= 10;
		l += *str - '0';
		str++;
	}
	i = (int)(l * n);
	return (i);
}
